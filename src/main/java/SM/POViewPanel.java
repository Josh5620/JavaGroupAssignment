    private void loadPOs() {
        try {
            DefaultTableModel model = (DefaultTableModel) myTable.getModel();
            model.setRowCount(0); 
            List<String[]> pos = new POManager().loadPOs(); 
            for (String[] po : pos) {
                model.addRow(po);  
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error loading POs: " + e.getMessage());
        }
    }

