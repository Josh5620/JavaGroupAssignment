    public void buildUserTable(){
        userTable.getTableHeader().setReorderingAllowed(false);
        userTable.getTableHeader().setResizingAllowed(false);
        userTable.enableInputMethods(false);
        userTable.setCellSelectionEnabled(false);
        userTable.setColumnSelectionAllowed(false);
        userTable.setRowSelectionAllowed(false);
        
        // Refresh the user list from file 
        admin.reloadUsers();
        
        // Load the existing table model generated by Netbeans
        DefaultTableModel Usermodel = (DefaultTableModel) userTable.getModel();
        
        // Clear any existing rows
        Usermodel.setRowCount(0);
        
        // Add users from the UserList
        List<List<String>> userList = admin.getUserList();
        for (List<String> user: userList){
            Usermodel.addRow(new Object[]{
            user.get(0),
            user.get(1),
            user.get(2),
            user.get(3)
        });
        }
    }
    
    public void RoleFilter(){
        DefaultTableModel Usermodel = (DefaultTableModel) userTable.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(Usermodel);
        userTable.setRowSorter(sorter);
        
        String selectedRole = (String) RoleFilterBox.getSelectedItem();
        String rolecode = roleMap.get(selectedRole);
        
        if (selectedRole.equalsIgnoreCase("All")){
            sorter.setRowFilter(null);
        }
        else if (selectedRole.equalsIgnoreCase("Admin")){
            sorter.setRowFilter(RowFilter.regexFilter("(?i)^" + rolecode + "$", 3));
        }
        else if (selectedRole.equalsIgnoreCase("Sales Manager")){
            sorter.setRowFilter(RowFilter.regexFilter("(?i)^" + rolecode + "$", 3));
        }
        else if (selectedRole.equalsIgnoreCase("Purchase Manager")){
            sorter.setRowFilter(RowFilter.regexFilter("(?i)^" + rolecode + "$", 3));
        }
        else if (selectedRole.equalsIgnoreCase("Finance Manager")){
            sorter.setRowFilter(RowFilter.regexFilter("(?i)^" + rolecode + "$", 3));
        }
        else if (selectedRole.equalsIgnoreCase("Inventory Manager")){
            sorter.setRowFilter(RowFilter.regexFilter("(?i)^" + rolecode + "$", 3));
        }
        buildUserTable();
    }
    
public void launchRoleDashboard(String username, String password, String role, JFrame adminFrame) {
    switch (role) {
        case "SM" -> new SalesManagerDashboard(username, password, adminFrame).setVisible(true);
        case "PM" -> new PMForm1(username, password, adminFrame).setVisible(true);
        case "IM" -> new IMForm(username, password, adminFrame).setVisible(true);
        case "FM" -> new FMForm(username, password, adminFrame).setVisible(true);
        default   -> JOptionPane.showMessageDialog(null, "Invalid role!");
    }

    // now hide the login/admin frame
    if (adminFrame != null) {
        adminFrame.setVisible(false);
    }
}
    
